// Helper functions to get fixtures data
export const getFixturesByDate = (date) => {
    return fixturesData.filter(fixture => fixture.date === date)
}

export const getFixturesByDay = (day) => {
    return fixturesData.filter(fixture => fixture.day === day)
}

export const getFixturesByStatus = (status) => {
    return fixturesData.filter(fixture => fixture.status === status)
}

export const getUpcomingFixtures = () => {
    return fixturesData.filter(fixture => fixture.status === "Upcoming")
}

export const getRecentFixtures = (limit = 4) => {
    return fixturesData.slice(0, limit)
}

// Group fixtures by date for easier rendering
export const getFixturesGroupedByDate = () => {
    const grouped = {}
    fixturesData.forEach(fixture => {
        if (!grouped[fixture.date]) {
            grouped[fixture.date] = []
        }
        grouped[fixture.date].push(fixture)
    })
    return grouped
} 